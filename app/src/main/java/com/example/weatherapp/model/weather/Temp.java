package com.example.weatherapp.model.weather;

public class Temp {
    public double day;
    public double min;
    public double max;
    public double night;
    public double eve;
    public double morn;

    public double getDay() {
        return day;
    }

    public double getMin() {
        return min;
    }

    public double getMax() {
        return max;
    }

    public double getNight() {
        return night;
    }

    public double getEve() {
        return eve;
    }

    public double getMorn() {
        return morn;
    }
}
